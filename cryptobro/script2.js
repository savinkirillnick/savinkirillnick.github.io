function buyBoost(curr){

	var boost_cost = 50;
	const x = [1,3];
	const currencies = ["coins", "diamonds", "keys"];
	if (curr == "coins") {
		boost_cost = x[user["boosts"] % 2] * Math.pow(10, Math.floor(user["boosts"] / 2));
	}
	if (user[curr] < boost_cost) {
		createCongrat(translate["shop_no_buy"][user["lang"]], translate["skill_learn_no_fund"][user["lang"]]);
		return;
	}
	switch (currencies.indexOf(curr)){
		case 0:
			user.addCoins(-boost_cost);
			break;
		case 1:
			user.addDiamonds(-boost_cost);
			break;
		case 2:
			user.addKeys(-boost_cost);
			break;
		default:
			break;
	}
	user.plusTask("boost", 1);
	createCongrat(translate["shop_success"][user["lang"]], translate["shop_success_gas"][user["lang"]]);
	if (curr == "coins") {user["boosts"] += 1;}
	user["energy"] = user.getFullEnergy();
	viewActivity(user['activity_window']);
}

function buyBuff(buff_name){
	const cost = User.buffs[buff_name]["cost"];
	const curr = User.buffs[buff_name]["currency"];
	const currencies = ["coins", "diamonds", "keys"];
	if (user[curr] < cost) {
		createCongrat(translate["shop_no_buy"][user["lang"]], translate["skill_learn_no_fund"][user["lang"]]);
		return;
	}

	if (user.getBuff(buff_name) > user["activity_time"]){
		createCongrat(translate["shop_no_buy"][user["lang"]], translate["shop_same_buff"][user["lang"]]);
		return;
	}
	const buff_data = JSON.parse(user.buffs);
	var num_active_buffs = 0;
	for (const [key, value] of Object.entries(buff_data)) {
		if (value > (new Date()).getTime()) {
			num_active_buffs += 1;
		}
	}
	if (num_active_buffs >= user.getMaxBuff()){
		createCongrat(translate["shop_no_buy"][user["lang"]], translate["shop_max_num_buff"][user["lang"]] + " " + num_active_buffs + "!");
		return;
	}

	switch (currencies.indexOf(curr)){
		case 0:
			user.addCoins(-boost_cost);
			break;
		case 1:
			user.addDiamonds(-boost_cost);
			break;
		case 2:
			user.addKeys(-boost_cost);
			break;
		default:
			break;
	}
	user.plusTask("buff", 1);
	user.setBuff(buff_name, user["activity_time"] + 15*60*1000);
	createCongrat(translate["shop_success"][user["lang"]], translate["shop_success_desc"][user["lang"]]);
	viewActivity(user['activity_window']);
}


