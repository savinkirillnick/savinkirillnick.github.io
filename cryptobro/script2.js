function calculateLoadingProgress() {
  const sources = window.performance;
  if (!sources || !sources.getEntriesByType) {
    return 100;
  }
  const count_sources = sources.getEntriesByType("resource").length;
  const loading_sources = sources.getEntriesByType("resource").filter(loader => loader.initiatorType !== "img" || loader.responseEnd > 0).length;
  const result = count_sources > 0 ? loading_sources / count_sources * 100 : 100;
  return Math.round(result);
}

function animate() {
  if (count == 100) {
    clearInterval(loading);
    document.getElementById("loader").style.display = "None";
  } else {
    if (count < calculateLoadingProgress()) {
      count += 4;
    }
    w = 100 - count;
    document.getElementById("animate").style.width = w + '%';
  }
}


//Cookie functions
function setCookie(name,value,days) {
    var expires = "";
    if (days) {
        var date = new Date();
        date.setTime(date.getTime() + (days*24*60*60*1000));
        expires = "; expires=" + date.toUTCString();
    }
    document.cookie = name + "=" + (value || "")  + expires + "; path=/";
}
function getCookie(name) {
    var nameEQ = name + "=";
    var ca = document.cookie.split(';');
    for(var i=0;i < ca.length;i++) {
        var c = ca[i];
        while (c.charAt(0)==' ') c = c.substring(1,c.length);
        if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length,c.length);
    }
    return null;
}
function eraseCookie(name) {   
    document.cookie = name +'=; Path=/; Expires=Thu, 01 Jan 1970 00:00:01 GMT;';
}

//Number functions
function formatNumber(num) {
	if (num === 0) return '0';

	const k = 1000;
	const m = k * 1000;
	const b = m * 1000;
	const t = b * 1000;

	if (num >= t) {
		return (num / t).toFixed(1) + ' T';
	} else if (num >= b) {
		return (num / b).toFixed(1) + ' B';
	} else if (num >= m) {
		return (num / m).toFixed(1) + ' M';
	} else if (num >= k) {
		return (num / k).toFixed(1) + ' K';
	}

	return num.toString();
}

function formatNumberWithCommas(number) {
	if (isNaN(number)) {
		return 'Введите корректное число';
	}

	// Преобразуем число в строку и удаляем возможные нецифровые символы
	const numberStr = number.toString().replace(/[^0-9]/g, '');

	// Используем регулярное выражение для вставки пробелов
	return numberStr.replace(/\B(?=(\d{3})+(?!\d))/g, ' ');
}

function formatSeconds(totalSeconds) {
	totalSeconds /= 1000;
    const secondsInASecond = 1;
    const secondsInAMinute = 60;
    const secondsInAnHour = 3600;
    const secondsInADay = 86400;

    const days = Math.floor(totalSeconds / secondsInADay);
    totalSeconds %= secondsInADay;

    const hours = Math.floor(totalSeconds / secondsInAnHour);
    totalSeconds %= secondsInAnHour;

    const minutes = Math.floor(totalSeconds / secondsInAMinute);
    const seconds = Math.floor(totalSeconds % secondsInAMinute);

    let result = "";

    if (days > 0) {
        result += `${days}:`;
    }

    if (hours > 0 || days > 0) {
        result += String(hours).padStart(2, '0') + ":";
    } else if (hours == 0 && days == 0){
    	
    } else {
        result += "00:";
    }

    result += String(minutes).padStart(2, '0') + ":";
    result += String(seconds).padStart(2, '0');

    return result;
}

