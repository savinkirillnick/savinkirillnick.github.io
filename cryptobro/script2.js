function exchangeTo(base, quote) {
	var data_assets = {
		"stars": "⭐️",
		"coins": "💠",
		"diamonds": "💎",
		"keys": "🔑",
		"usdt": "💵",
	};

	if (quote === "usdt") {
		createCongrat(translate["shop_no_buy"][user["lang"]], translate["exchange_usdt"][user["lang"]]);
		return;
	}

	const pair = base + quote;
	var k1 = 1;
	var k2 = User.courses[pair];
	while (k2 < 1) {
		k1 *= 10;
		k2 *= 10;
	}
	var transaction = document.createElement('div');
	transaction.id = "cardId";
	transaction.className = 'fullscreen-div';
	var text = `<div id="myId" class="centered-div">
	<div class="congratulation-name">${name}</div>
	<div class="congratulation-desc">${data_assets[base]}${k1} ${translate["exchange_to"][user["lang"]]} ${data_assets[quote]}${k2}</div>

	<div class="shop_container">
		<div class="card_desc">${translate["exchange_add"][user["lang"]]}<br>
		<input id="ex_in" type="number" min="0" step="1" oninput="exCalculate('b', '${base}', ${User.courses[pair]});"></div>
		<div class="card_desc">${translate["exchange_result"][user["lang"]]}<br>
		<input id="ex_res" type="number" min="0" step="1" oninput="exCalculate('q', '${base}', ${User.courses[pair]});"></div>
	</div>

	<div class="congratulation-button" id="exchange_button" onClick="swap('${base}', '${quote}');">${translate["exchange_button"][user["lang"]]}</div>

	<div class="congratulation-button" id="close_button">${translate["note_close"][user["lang"]]}</div></div>`;

	transaction.innerHTML = text;

	document.body.appendChild(transaction);
	document.getElementById('close_button').addEventListener('click', function(){
		transaction.remove();
	});

	setTimeout(function(){
		document.getElementById('myId').style.transform = "translate(-50%, -50%)";
		document.getElementById('myId').style.opacity = "1";
		document.getElementById('myId').style.maxWidth = "500px";
		document.getElementById('myId').style.width = "80%";
		document.getElementById('myId').style.height = "auto";
		transaction.style.background = "rgba(0, 0, 0, 0.9)";
	}, 0);
}

function exCalculate(side, base, course) {
	const depo_b = user[base];
	const input_base = document.getElementById('ex_in');
	const input_quote = document.getElementById('ex_res');
	if (side === "b") {
		document.getElementById('ex_res').value = Math.floor(input_base.valueAsNumber * course);
	} else if (side === "q") {
		document.getElementById('ex_in').value = Math.ceil(input_quote.valueAsNumber / course);
	}
}

function swap(base, quote) {
	const accets = ['stars', 'coins', 'diamonds', 'keys']
	const pair = base + quote;
	const base_qty = document.getElementById('ex_in').valueAsNumber;
	const quote_qty = Math.floor(input_base.valueAsNumber * User.courses[pair]);
	if (user[base] >= base_qty) {
		switch (base){
			case 0:
				user.addStars(-base_qty);
				break;
			case 1:
				user.addCoins(-base_qty);
				break;
			case 2:
				user.addDiamonds(-base_qty);
				break;
			case 3:
				user.addKeys(-base_qty);
				break;
		}
		switch (quote){
			case 0:
				user.addStars(quote_qty);
				break;
			case 1:
				user.addCoins(quote_qty);
				break;
			case 2:
				user.addDiamonds(quote_qty);
				break;
			case 3:
				user.addKeys(quote_qty);
				break;
		}
		createCongrat(translate["shop_success"][user["lang"]], translate["exchange_success"][user["lang"]]);
	} else {
		createCongrat(translate["shop_no_buy"][user["lang"]], translate["exchange_no_funds"][user["lang"]]);
	}
}

